// ASSIGNMENT OPERATORS

// (=) Operator

let x = 10 // x assigns to 10.

// (+=) Operator

x += 5 // Adds a value to a variable (10(x) + 5 = 15)

// (-=) Operator

x -= 5 // It subtracts now. (15(x) - 5 = 10)

// (*=) Operator

x *= 5 // Multiplies a value to a variable (10(x) * 5 = 50)

// (/=) Operator

x /= 5 // It divides now. (50(x) / 5 = 10)

// (%=) Operator

x %= 4 // It assigns the remainder (rest) of the division (10(x) / 4 = 2 r:2 because r:2 * 4 + 2 = 10(x)) 

console.log(x)